package net.zdsoft.bigdata.datav.parameter;

/**
 * @author shenke
 * @since 2018/10/17 20:02
 */
public class GroupEffectScatter {

    private Integer effectScatterPeriod;
    private Integer effectScatterScale;
    private String effectScatterType;
    private String effectScatterColor;

    public Integer getEffectScatterPeriod() {
        return effectScatterPeriod;
    }

    public void setEffectScatterPeriod(Integer effectScatterPeriod) {
        this.effectScatterPeriod = effectScatterPeriod;
    }

    public Integer getEffectScatterScale() {
        return effectScatterScale;
    }

    public void setEffectScatterScale(Integer effectScatterScale) {
        this.effectScatterScale = effectScatterScale;
    }

    public String getEffectScatterType() {
        return effectScatterType;
    }

    public void setEffectScatterType(String effectScatterType) {
        this.effectScatterType = effectScatterType;
    }

    public String getEffectScatterColor() {
        return effectScatterColor;
    }

    public void setEffectScatterColor(String effectScatterColor) {
        this.effectScatterColor = effectScatterColor;
    }
}
