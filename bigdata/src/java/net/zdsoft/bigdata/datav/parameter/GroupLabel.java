package net.zdsoft.bigdata.datav.parameter;

/**
 * @author shenke
 * @since 2018/10/16 10:15
 */
public class GroupLabel {

    private Boolean labelShow;
    private Boolean labelLineShow;
    private Integer labelLineLength;
    private String labelFontColor;
    private Integer labelFontSize;
    private String labelFontWeight;
    private String labelFormatter;
    private String labelPosition;

    public Boolean getLabelShow() {
        return labelShow;
    }

    public void setLabelShow(Boolean labelShow) {
        this.labelShow = labelShow;
    }

    public Boolean getLabelLineShow() {
        return labelLineShow;
    }

    public void setLabelLineShow(Boolean labelLineShow) {
        this.labelLineShow = labelLineShow;
    }

    public Integer getLabelLineLength() {
        return labelLineLength;
    }

    public void setLabelLineLength(Integer labelLineLength) {
        this.labelLineLength = labelLineLength;
    }

    public String getLabelFontColor() {
        return labelFontColor;
    }

    public void setLabelFontColor(String labelFontColor) {
        this.labelFontColor = labelFontColor;
    }

    public Integer getLabelFontSize() {
        return labelFontSize;
    }

    public void setLabelFontSize(Integer labelFontSize) {
        this.labelFontSize = labelFontSize;
    }

    public String getLabelFontWeight() {
        return labelFontWeight;
    }

    public void setLabelFontWeight(String labelFontWeight) {
        this.labelFontWeight = labelFontWeight;
    }

    public String getLabelFormatter() {
        return labelFormatter;
    }

    public void setLabelFormatter(String labelFormatter) {
        this.labelFormatter = labelFormatter;
    }

    public String getLabelPosition() {
        return labelPosition;
    }

    public void setLabelPosition(String labelPosition) {
        this.labelPosition = labelPosition;
    }
}
