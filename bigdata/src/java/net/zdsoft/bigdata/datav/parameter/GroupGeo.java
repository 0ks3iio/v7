public class GroupGeo {
    private String normalColor;
    private String emphasisColor;
    private String borderColor;
    private Integer borderWidth;
    private String borderType;
    private Boolean mapLabelShow;
    private String mapLabelFontColor;
    private Integer mapLabelFontSize;

    public String getNormalColor() {
        return normalColor;
    }

    public void setNormalColor(String normalColor) {
        this.normalColor = normalColor;
    }

    public String getEmphasisColor() {
        return emphasisColor;
    }

    public void setEmphasisColor(String emphasisColor) {
        this.emphasisColor = emphasisColor;
    }

    public String getBorderColor() {
        return borderColor;
    }

    public void setBorderColor(String borderColor) {
        this.borderColor = borderColor;
    }

    public Integer getBorderWidth() {
        return borderWidth;
    }

    public void setBorderWidth(Integer borderWidth) {
        this.borderWidth = borderWidth;
    }

    public String getBorderType() {
        return borderType;
    }

    public void setBorderType(String borderType) {
        this.borderType = borderType;
    }

    public Boolean getMapLabelShow() {
        return mapLabelShow;
    }

    public void setMapLabelShow(Boolean mapLabelShow) {
        this.mapLabelShow = mapLabelShow;
    }

    public String getMapLabelFontColor() {
        return mapLabelFontColor;
    }

    public void setMapLabelFontColor(String mapLabelFontColor) {
        this.mapLabelFontColor = mapLabelFontColor;
    }

    public Integer getMapLabelFontSize() {
        return mapLabelFontSize;
    }

    public void setMapLabelFontSize(Integer mapLabelFontSize) {
        this.mapLabelFontSize = mapLabelFontSize;
    }
}
