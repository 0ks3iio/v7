package net.zdsoft.bigdata.datav.parameter;

/**
 * @author shenke
 * @since 2018/10/17 20:17
 */
public class GroupLines {

    private Integer linesPeriod;
    private String linesColor;
    private Integer linesWidth;

    public Integer getLinesPeriod() {
        return linesPeriod;
    }

    public void setLinesPeriod(Integer linesPeriod) {
        this.linesPeriod = linesPeriod;
    }

    public String getLinesColor() {
        return linesColor;
    }

    public void setLinesColor(String linesColor) {
        this.linesColor = linesColor;
    }

    public Integer getLinesWidth() {
        return linesWidth;
    }

    public void setLinesWidth(Integer linesWidth) {
        this.linesWidth = linesWidth;
    }
}
