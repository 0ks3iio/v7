public class GroupTimelineStyle {

    private String timelineLineColor;
    private Integer timelineLineWidth;
    private String timelineLineType;
    private String timelinePointColor;
    private Integer timelinePointSize;
    private String timelineTextColor;
    private String timelineControlColor;
    private String timelineOrient;

    public String getTimelineOrient() {
        return timelineOrient;
    }

    public void setTimelineOrient(String timelineOrient) {
        this.timelineOrient = timelineOrient;
    }

    public String getTimelineControlColor() {
        return timelineControlColor;
    }

    public void setTimelineControlColor(String timelineControlColor) {
        this.timelineControlColor = timelineControlColor;
    }

    public String getTimelineLineColor() {
        return timelineLineColor;
    }

    public void setTimelineLineColor(String timelineLineColor) {
        this.timelineLineColor = timelineLineColor;
    }

    public Integer getTimelineLineWidth() {
        return timelineLineWidth;
    }

    public void setTimelineLineWidth(Integer timelineLineWidth) {
        this.timelineLineWidth = timelineLineWidth;
    }

    public String getTimelineLineType() {
        return timelineLineType;
    }

    public void setTimelineLineType(String timelineLineType) {
        this.timelineLineType = timelineLineType;
    }

    public String getTimelinePointColor() {
        return timelinePointColor;
    }

    public void setTimelinePointColor(String timelinePointColor) {
        this.timelinePointColor = timelinePointColor;
    }

    public Integer getTimelinePointSize() {
        return timelinePointSize;
    }

    public void setTimelinePointSize(Integer timelinePointSize) {
        this.timelinePointSize = timelinePointSize;
    }

    public String getTimelineTextColor() {
        return timelineTextColor;
    }

    public void setTimelineTextColor(String timelineTextColor) {
        this.timelineTextColor = timelineTextColor;
    }
}
