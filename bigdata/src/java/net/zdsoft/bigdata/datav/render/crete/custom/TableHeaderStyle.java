public class TableHeaderStyle {

    private Integer headerHeight;
    private String headerBackgroundColor;
    private Integer headerFontSize;
    private String headerFontColor;
    private String headerFontBold;

    public Integer getHeaderHeight() {
        return headerHeight;
    }

    public void setHeaderHeight(Integer headerHeight) {
        this.headerHeight = headerHeight;
    }

    public String getHeaderBackgroundColor() {
        return headerBackgroundColor;
    }

    public void setHeaderBackgroundColor(String headerBackgroundColor) {
        this.headerBackgroundColor = headerBackgroundColor;
    }

    public Integer getHeaderFontSize() {
        return headerFontSize;
    }

    public void setHeaderFontSize(Integer headerFontSize) {
        this.headerFontSize = headerFontSize;
    }

    public String getHeaderFontColor() {
        return headerFontColor;
    }

    public void setHeaderFontColor(String headerFontColor) {
        this.headerFontColor = headerFontColor;
    }

    public String getHeaderFontBold() {
        return headerFontBold;
    }

    public void setHeaderFontBold(String headerFontBold) {
        this.headerFontBold = headerFontBold;
    }
}
