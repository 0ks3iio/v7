package net.zdsoft.exammanage.data.dto;

public class EmGoodCountDto {

    private String schoolName;
    private String unitTopName;
    private int count;

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public String getUnitTopName() {
        return unitTopName;
    }

    public void setUnitTopName(String unitTopName) {
        this.unitTopName = unitTopName;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}
