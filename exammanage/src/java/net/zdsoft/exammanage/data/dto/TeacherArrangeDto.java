package net.zdsoft.exammanage.data.dto;

public class TeacherArrangeDto {

    private String teacherName;
    private String examNames;
    private String examSize;

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getExamNames() {
        return examNames;
    }

    public void setExamNames(String examNames) {
        this.examNames = examNames;
    }

    public String getExamSize() {
        return examSize;
    }

    public void setExamSize(String examSize) {
        this.examSize = examSize;
    }


}
