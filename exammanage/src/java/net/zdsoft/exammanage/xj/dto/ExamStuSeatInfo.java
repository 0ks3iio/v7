public class ExamStuSeatInfo {

    private String examTime;
    private String examPlacd;
    private String examStuNum;
    private String examName;

    public String getExamTime() {
        return examTime;
    }

    public void setExamTime(String examTime) {
        this.examTime = examTime;
    }

    public String getExamPlacd() {
        return examPlacd;
    }

    public void setExamPlacd(String examPlacd) {
        this.examPlacd = examPlacd;
    }

    public String getExamStuNum() {
        return examStuNum;
    }

    public void setExamStuNum(String examStuNum) {
        this.examStuNum = examStuNum;
    }

    public String getExamName() {
        return examName;
    }

    public void setExamName(String examName) {
        this.examName = examName;
    }


}
