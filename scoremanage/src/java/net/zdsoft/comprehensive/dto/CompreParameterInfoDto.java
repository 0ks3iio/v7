package net.zdsoft.comprehensive.dto;

public class CompreParameterInfoDto {
	private String keyName;
	private int lowNum;
	private int upNum;
	private Float score;
	public String getKeyName() {
		return keyName;
	}
	public void setKeyName(String keyName) {
		this.keyName = keyName;
	}
	public int getLowNum() {
		return lowNum;
	}
	public void setLowNum(int lowNum) {
		this.lowNum = lowNum;
	}
	public int getUpNum() {
		return upNum;
	}
	public void setUpNum(int upNum) {
		this.upNum = upNum;
	}
	public Float getScore() {
		return score;
	}
	public void setScore(Float score) {
		this.score = score;
	}

	
}
