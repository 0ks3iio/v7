package net.zdsoft.eclasscard.data.dto;

public class DormBuildingStuNumDto {

	private String buildingName;
	private int roomNums;
	private int studentNums;

	public String getBuildingName() {
		return buildingName;
	}

	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}

	public int getRoomNums() {
		return roomNums;
	}

	public void setRoomNums(int roomNums) {
		this.roomNums = roomNums;
	}

	public int getStudentNums() {
		return studentNums;
	}

	public void setStudentNums(int studentNums) {
		this.studentNums = studentNums;
	}

}
