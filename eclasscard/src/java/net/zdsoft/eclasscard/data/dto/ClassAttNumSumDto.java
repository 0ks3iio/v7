package net.zdsoft.eclasscard.data.dto;

public class ClassAttNumSumDto {
	private String studentId;
	private String studentName;
	private int qjNum;
	private int cdNum;
	private int qkNum;

	public String getStudentId() {
		return studentId;
	}

	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public int getQjNum() {
		return qjNum;
	}

	public void setQjNum(int qjNum) {
		this.qjNum = qjNum;
	}

	public int getCdNum() {
		return cdNum;
	}

	public void setCdNum(int cdNum) {
		this.cdNum = cdNum;
	}

	public int getQkNum() {
		return qkNum;
	}

	public void setQkNum(int qkNum) {
		this.qkNum = qkNum;
	}

}
